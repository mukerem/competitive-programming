a = """+---+---+---+---+---+---+---+---+
|...|:::|...|:::|...|:::|...|:::|
+---+---+---+---+---+---+---+---+
|:::|...|:::|...|:::|...|:::|...|
+---+---+---+---+---+---+---+---+
|...|:::|...|:::|...|:::|...|:::|
+---+---+---+---+---+---+---+---+
|:::|...|:::|...|:::|...|:::|...|
+---+---+---+---+---+---+---+---+
|...|:::|...|:::|...|:::|...|:::|
+---+---+---+---+---+---+---+---+
|:::|...|:::|...|:::|...|:::|...|
+---+---+---+---+---+---+---+---+
|...|:::|...|:::|...|:::|...|:::|
+---+---+---+---+---+---+---+---+
|:::|...|:::|...|:::|...|:::|...|
+---+---+---+---+---+---+---+---+"""
b = [list(j) for j in a.split('\n')]
x = input()[7:]
y = input()[7:]
if x:
    x = x.split(',')
if y:
    y = y.split(',')
for i in x:
    if len(i) == 2:
        z = 'P'
    else:
        z = i[0].upper()
    u = ord(i[-2]) - 97
    v = int(i[-1]) - 1
    b[15 - 2*v][4*u + 2] = z
    
for i in y:
    if len(i) == 2:
        z = 'p'
    else:
        z = i[0].lower()
    u = ord(i[-2]) - 97
    v = int(i[-1]) - 1
    b[15 - 2*v][4*u + 2] = z
    
for i in b:
    print(''.join(i))
